
class Article {
  String title;
  String description;
  DateTime publishedAt;
  String author;
  String url;
  String urlToImage;
  String content;

  // Constructor
  Article({
    required this.title,
    required this.description,
    required this.publishedAt,
    required this.author,
    required this.url,
    required this.urlToImage,
    required this.content,
  });
}
